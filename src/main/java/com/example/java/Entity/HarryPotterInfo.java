package com.example.java.Entity;

public class HarryPotterInfo {
    private int harryPotterMovieCount;  // 哈利波特电影数量
    private int formatCount;            // 版本数量
    private int webPageCount;           // 网页数量
    private int mergedWebPageCount;     // 合并的网页数量

    // Getters and Setters
    public int getHarryPotterMovieCount() {
        return harryPotterMovieCount;
    }

    public void setHarryPotterMovieCount(int harryPotterMovieCount) {
        this.harryPotterMovieCount = harryPotterMovieCount;
    }

    public int getFormatCount() {
        return formatCount;
    }

    public void setFormatCount(int formatCount) {
        this.formatCount = formatCount;
    }

    public int getWebPageCount() {
        return webPageCount;
    }

    public void setWebPageCount(int webPageCount) {
        this.webPageCount = webPageCount;
    }

    public int getMergedWebPageCount() {
        return mergedWebPageCount;
    }

    public void setMergedWebPageCount(int mergedWebPageCount) {
        this.mergedWebPageCount = mergedWebPageCount;
    }
}

